def estado_to_uf(estado):
    if estado == 'Acre':
        return "AC"
    elif estado == 'Alagoas':
        return "AL"
    elif estado == 'Amapá':
        return "AP"
    elif estado == 'Amazonas':
        return "AM"
    elif estado == 'Bahia':
        return "BA"
    elif estado == 'Ceara':
        return "CE"
    elif estado == 'Distrito Federal':
        return "DF"
    elif estado == 'Espírito Santo':
        return "ES"
    elif estado == 'Goiás':
        return "GO"
    elif estado == 'Maranhão':
        return "MA"
    elif estado == 'Mato Grosso':
        return "MT"
    elif estado == 'Mato Grosso do Sul':
        return "MS"
    elif estado == 'Minas Gerais':
        return "MG"
    elif estado == 'Pará':
        return "PA"
    elif estado == 'Paraíba':
        return "PB"
    elif estado == 'Paraná':
        return "PR"
    elif estado == 'Pernambuco':
        return "PE"
    elif estado == 'Piauí':
        return "PI"
    elif estado == 'Rio de Janeiro':
        return "RJ"
    elif estado == 'Rio Grande do Norte':
        return "RN"
    elif estado == 'Rio Grande do Sul':
        return "RS"
    elif estado == 'Rondônia':
        return "RO"
    elif estado == 'Roraima':
        return "RR"
    elif estado == 'Santa Catarina':
        return "SC"
    elif estado == 'São Paulo':
        return "SP"
    elif estado == 'Sergipe':
        return "SE"
    elif estado == 'Tocantins':
        return "TO"